# Find even numbers in a list
number = [23, 45, 34, 76, 100, 67, 83, 250, 88, 65]

for i in number:
    if i % 2 == 0:  # Check if number is even
        print(f"Here are the even numbers: {i}")

#Output:
#Here are the even numbers: 34
#Here are the even numbers: 76
#Here are the even numbers: 100
#Here are the even numbers: 250
#Here are the even numbers: 88
